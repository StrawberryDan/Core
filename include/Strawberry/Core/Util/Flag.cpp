#include "Flag.hpp"

//======================================================================================================================
//  Class Definitions
//----------------------------------------------------------------------------------------------------------------------
namespace Strawberry::Core
{
	Flag::Flag(bool value)
		: mIsSet(value)
	{}

	void Flag::Set()
	{
		mIsSet = true;
	}

	void Flag::Unset()
	{
		mIsSet = false;
	}

	Flag& Flag::operator=(bool value)
	{
		mIsSet = value;
		return *this;
	}
} // namespace Strawberry::Core