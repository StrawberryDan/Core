#pragma once
//======================================================================================================================
//  Includes
//----------------------------------------------------------------------------------------------------------------------
#include <concepts>
#include <limits>


//======================================================================================================================
//  Class Declaration
//----------------------------------------------------------------------------------------------------------------------
namespace Strawberry::Core
{
	template<std::integral T, T Null>
	class NullValue {};


	template<std::integral T>
	using NonZero = NullValue<T, T{0}>;

	template<std::integral T>
	using NonMax = NullValue<T, std::numeric_limits<T>::max()>;
}
